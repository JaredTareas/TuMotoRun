private void cargarRoles() {
    try {
     conexion.conexionMysql con = new conexion.conexionMysql();
    Connection conectar = con.conexion();
        String sql = "SELECT id_rol, nombre_rol FROM roles";
        PreparedStatement ps = conectar.prepareStatement(sql);
        ResultSet rs = ps.executeQuery();

        boxrol.removeAllItems(); // Limpia el combo primero

        while (rs.next()) {
            int id = rs.getInt("id_rol");
            String nombre = rs.getString("nombre_rol");
            boxrol.addItem(new RolItem(id, nombre)); // Agrega objetos RolItem
        }

        rs.close();
        ps.close();
        conectar.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Error cargando roles: " + e.getMessage());
    }
}








